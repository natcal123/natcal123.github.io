var germanyButton = document.getElementById("germanyButton");

germanyButton.addEventListener("mouseenter", handleBtnHoverGermany);

function handleBtnHoverGermany() {
  var items = document.querySelectorAll('.video');
  items.forEach(function(item) {
    item.style.display = "none";
  });
  var itemsshow = document.querySelectorAll('.country-germany');
  itemsshow.forEach(function(item) {
    item.style.display = 'inline-block';
    // item.classList.add('bigger-videos');
  });
}

